<br><br>
Kurs nauki klawiszy jest dość dobrze zaplanowany, choć posiada kilka błędów - czasem w lekcji pojawiają się litery jeszcze nie przerobione. Na plus za to można zaliczyć możliwość ćwiczenia konkretnych palców oraz naukę szybkiego wprowadzania cyfr za pomocą klawiatury numerycznej. Po nauczeniu się podstaw pisania, można ćwiczyć przepisywanie tekstów, a ich proponowany wybór jest bardzo szeroki (literatura piękna, poradniki, eseje). Niestety, są one zbyt długie i program błędnie ocenia twoją prędkość pisania. Błąd ten pojawia się w wersji 1.00 programu i daje o sobie znać również wtedy, gdy ładujesz własne teksty o dużej objętości, przez co do przepisywania nadają się tylko krótkie. Na szczęście można go naprawić poprzez ściągnięcie patcha ze strony firmy Nahlik Soft.
<br><br>
Do zalet Mistrza Klawiatury można zaliczyć spory i przydatny plik pomocy (choć mniejszy niż w Mistrzu Klawiatury II). Znajdują się w nim między innymi wskazówki co do tego, jak przygotować sobie warunki pracy i co zrobić, aby pisać jak najszybciej. Inną bardzo ciekawą opcją jest wyświetlenie statystyk dotyczących twojego pisania - możesz poznać prędkość i poprawność, z jaką naciskasz poszczególne klawisze, po czym przećwiczyć te, z którymi jeszcze nie radzisz sobie zbyt dobrze.
<br><br>
Podsumowując, Mistrz Klawiatury to dobry program, dzięki któremu łatwo nauczysz się pisania na klawiaturze od podstaw.
<br><br>
JAK KORZYSTAĆ Z PROGRAMU?
<br><br>
Jest to dość proste. Najpierw zarejestruj się, żeby mieć później dostęp do statystyk. Aby rozpocząc naukę pisania od podstaw, wybierz opcję LEKCJA -&gt; WYBÓR LEKCJI -&gt; NAUKA KLAWISZY ASDF. Zapoznaj się z informacją wyświetloną w okienku, aby odpowiednio ułożyć palce, po czym wciśnij spację lub kliknij na OK. Lekcja polega na przepisywaniu tekstu, który pojawiać się będzie na ekranie. Pamiętaj, żeby nie patrzeć na klawiaturę! Po przepisaniu całego wiersza należy wcisnąć ENTER. Kiedy przepiszesz już cały tekst, pojawią się wyniki. Jeśli będą one dobre, tzn. prędkość około 100 znaków na minutę i mało błędów (poprawność ponad 98%), możesz przejść do następnej lekcji. Jeśli nie - powtórz lekcję, aż do osiągnięcia dobrych wyników. W ten sposób powinieneś przerabiać kolejne lekcje, z góry na dół. Pamiętaj, żeby nie przechodzić do następnej lekcji, dopóki nie osiągniesz dobrych wyników w aktualnej. Kiedy przerobisz już wszystkie lekcje (z nauki cyfr i wprawek dla palców możesz jednak zrezygnować), możesz zabrać się za przepisywanie &quot;normalnych&quot; tekstów literackich. W tym celu skorzystaj z opcji LEKCJA -&gt; WŁASNE TEKSTY.
