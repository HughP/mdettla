Jest on największym i najbardziej rozbudowanym programem z tych tutaj opisywanych. Za jego pomocą można nauczyć się szybkiego pisania na wiele sposobów. Pierwszy z nich, to wybór odpowiedniego kursu składającego się z szeregu lekcji i konsekwentne jego realizowanie. Mistrz Klawiatury II umie bardzo dobrze ocenić twoje postępy w nauce i zawsze doradzi, czy możesz już przejść do kolejnych ćwiczeń, czy też powinieneś jeszcze jakieś elementy doszlifować. Chwilami miałem wrażenie, że czuwa nade mną prawdziwy nauczyciel. Oprócz szybkości pisania i poprawności, program bierze pod uwagę rytmikę pisania, która także ma duże znaczenie. Warto nadmienić, że do wyboru dostępne są różne rodzaje kursów. Inny dla całkiem początkujących, inny dla osób uzdolnionych manualnie, inny z kolei dla chcących używać klawiatury do innych celów niż pisanie.
